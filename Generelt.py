## Logical operations har typisk følgende symboler: >, <, ==, !, <=, => og bruges typisk med AND ; OR ; NOT

## And brugers når to muligheder er sand. 
# Eksempelvis for AND gælder false and false F, false and true F, true and false F, true and true T 
print(5 > 4 and 4 == 4)

## Or bruges når blot en af mulighederne er sand. 
# Eksempelvis for or gælder false or false F, false or true T, true or false T, true or true T 
print(5 > 10 or 5 < 10)

